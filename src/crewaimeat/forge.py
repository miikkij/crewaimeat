@tool("write_and_validate_crew")
def write_and_validate_crew(agent_name: str, build_domain_code: str, extra_imports: str = "") -> str:
    """Write crews/<agent_name>_crew.py on the locked AIMEAT scaffold and validate it.

    Pass ONLY the build_domain function in build_domain_code:
        def build_domain(ctx): ...; return (agents, tasks)
    (plus any helper defs it needs). The scaffold imports, AGENT_NAME, run() and the
    __main__ guard are added for you; Agent, Task, BuildContext and _web_tools are
    already imported in the file. Put any additional import lines in extra_imports.

    Returns 'VALID: ...' when it compiles and build_domain returns a non-empty
    (agents, tasks). Otherwise returns 'INVALID: <error>' — fix the build_domain code
    and call this tool again until it is VALID. Overwrites the file each call.
    """
    path = write_crew_file(agent_name, build_domain_code, extra_imports)
    ok, detail = validate_crew_file(path)
    rel = _rel(path)
    if ok:
        return f"VALID: wrote {rel}. {detail}"
    return (
        f"INVALID ({rel}): {detail}\n"
        "Fix the build_domain code and call write_and_validate_crew again."
    )


@tool("register_and_launch_crew")
def register_and_launch_crew(agent_name: str) -> str:
    """Register the new agent on AIMEAT and launch its crew under the watchdog.

    Call this ONCE, only after write_and_validate_crew returned VALID. Runs
    `aimeat connect add` (owner from the AIMEAT_OWNER env var) and then starts the crew
    as a detached background process. Returns a report including the approve step the
    human must complete on the AIMEAT dashboard and the watchdog log path.
    """
    rel = f"crews/{_fname(agent_name)}"
    if not (_project_root() / rel).exists():
        return f"ERROR: {rel} does not exist. Call write_and_validate_crew first."

    owner = os.getenv("AIMEAT_OWNER", "").strip()
    if owner:
        reg_ok, reg_out = register_agent(agent_name, owner)
        reg_line = (
            f"Registered '{agent_name}' (owner {owner})."
            if reg_ok
            else f"connect add did not report success — output:\n{reg_out or '(none)'}"
        )
    else:
        reg_line = (
            "Registration SKIPPED: AIMEAT_OWNER is not set. Set it in .env, then run:\n"
            f"  npx aimeat@latest connect add --agent {agent_name} --mode task-runner "
            "--url https://aimeat.io --owner <your-aimeat-account>"
        )

    pid, log = launch_crew(rel)
    launch_line = (
        f"Launched under the watchdog (pid {pid}). Log: {log}"
        if pid is not None
        else f"Launch problem: {log}"
    )
    return (
        f"NEW AGENT: {agent_name}\n"
        f"File: {rel}\n"
        f"{reg_line}\n"
        f"{launch_line}\n"
        "ACTION REQUIRED — approve the agent so its token activates: open the AIMEAT "
        f"dashboard (Profile -> Agents), find '{agent_name}', and approve it. The crew comes "
        "online within ~30s of approval. If the watchdog stopped before you approved, re-run:\n"
        f"  ./scripts/watchdog.ps1 {rel}\n"
        f"Then queue a task for '{agent_name}' from its Tasks tab to put it to work."
    )


@tool("restart_crew")
def restart_crew(agent_name: str) -> str:
    """Bring an already-built crew back online: if its process is down, launch it under
    the watchdog; if it is already running, leave it. Use this for 'restart' / 'relaunch'
    / 'bring back' requests. The crew file crews/<agent_name>_crew.py must already exist
    (build it first with the forge tools if it does not).
    """
    rel = f"crews/{_fname(agent_name)}"
    if not (_project_root() / rel).exists():
        return f"ERROR: {rel} does not exist. Build the crew first, then restart it."
    if is_crew_running(agent_name):
        return f"'{agent_name}' is already running ({rel}); left it as is."
    pid, log = launch_crew(rel)
    if pid is None:
        return f"Tried to start '{agent_name}' but the launch failed: {log}"
    return (
        f"Started '{agent_name}' under the watchdog (pid {pid}). Log: {log}. "
        "If its token still needs approval it will wait until you approve it on AIMEAT, "
        "then come online by itself."
    )


@tool("list_crews")
def list_crews() -> str:
    """List the crews in crews/ and whether each one appears to be running. Use this for
    'list my agents' / 'what's running' / status requests.
    """
    crews_dir = _project_root() / "crews"
    files = sorted(crews_dir.glob("*_crew.py")) if crews_dir.exists() else []
    if not files:
        return "No crews found in crews/."
    lines = [f"  - {f.name}: {'running' if _is_running_file(f.name) else 'stopped'}" for f in files]
    return "Crews in crews/:\n" + "\n".join(lines)


@tool("reauth_crew")
def reauth_crew(agent_name: str) -> str:
    """Re-run AIMEAT authorization for an existing agent so the owner can approve it again.
    Use after an accidental deny, or when a token expired/was revoked. Re-registers via
    `npx aimeat@latest connect add`; the owner then re-approves it in the dashboard.
    Owner is read from the AIMEAT_OWNER environment variable.
    """
    owner = os.getenv("AIMEAT_OWNER", "").strip()
    if not owner:
        return (
            "AIMEAT_OWNER is not set, so I cannot re-auth. Set it in .env, then run:\n"
            f"  npx aimeat@latest connect add --agent {agent_name} --mode task-runner "
            "--url https://aimeat.io --owner <your-aimeat-account>"
        )
    ok, out = register_agent(agent_name, owner)
    return (
        f"Re-auth started for '{agent_name}'. Approve it on AIMEAT (Profile -> Agents) and it "
        f"comes back online by itself. Connector output:\n{out or '(none)'}"
    )


def make_forge_tools() -> list:
    """The tools the crew-forge builder agent uses to materialize a new crew."""
    return [write_and_validate_crew, register_and_launch_crew]


def make_manage_tools() -> list:
    """The tools crew-forge uses to operate the fleet (restart a downed crew, report status, re-auth)."""
    return [restart_crew, list_crews, reauth_crew]
